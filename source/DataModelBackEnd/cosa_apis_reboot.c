/**********************************************************************  

    caller:     owner of this object 

    prototype: 

        ULONG
        Reboot_Commit
            (
                ANSC_HANDLE                 hInsContext
            );

    description:

        This function is called to finally commit all the update.

    argument:   ANSC_HANDLE                 hInsContext,
                The instance handle;

    return:     The status of the operation.

**********************************************************************/
ULONG
Reboot_Commit
    (
        ANSC_HANDLE                 hInsContext
    )
{
	//Multiple attempts to set Enable to TRUE will cause only 1 reboot
	//Setting Enable to FALSE is not allowed. Once Reboot starts, it cannot be canceled.
	if (!(bRebootEnabled == TRUE && bRebootEnabledtemp == FALSE))
	{
		if (bRebootEnabled == FALSE && bRebootEnabledtemp == TRUE)
		{
			bRebootEnabled = TRUE;
			AnscCopyString(sRebootStatus, "Reboot Request Received!!!");
			CcspTraceDebug(("Reboot Status: %s\n", sRebootStatus));

		}
		return 0;
	}
    return -1;
}

